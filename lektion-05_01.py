# LEKTION 5. Sträng metoder

s = "python programmering"

# sträng Upper() metoden
print("python programmering".upper())
print(s.upper())

s1 = "KAPITEL"

# sträng Lower() metoden
print(s1.lower())

# sträng Title() metoden
print("python programmering".title())
print(s.title())

# sträng Capitalize() metoden
print("python programmering".capitalize())
print(s.capitalize())

# sträng Split() metoden
print("python programmering".split())
print(s.split())

# sträng Count() metoden
print("python programmering".count('r'))
print(s.count('r'))

# sträng Replace() metoden
print("python programmering".replace('r', '7'))
print(s.replace('r', '7'))


